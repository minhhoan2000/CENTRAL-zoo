function filter() {
   const Animals = $('input[name=AnimalsOp]:checked').val();
  
   const geography = $('input[name=GeographyOp]:checked').val();
   let tmp_data = JSON.parse(JSON.stringify(data_zoo)) // clone data_zoo
   if (Animals)
      tmp_data = tmp_data.filter(zoo => {
         return zoo.Animals == Animals
      });
  
   
   if (geography)
      tmp_data = tmp_data.filter(zoo => {
         return zoo.Geography == geography
      });

   return tmp_data;
}


