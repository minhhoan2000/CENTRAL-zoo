function getCurrentPage(currentPage) {
   start = (currentPage - 1) * perPage;
   end = currentPage * perPage;
}


function renderDatazoo() {
   if (!document.getElementById('content_item')) return false;
   html = '';
   let filtered_zoo = filter() || data_zoo;
   if (filtered_zoo.length == 0) {
      html += '<div class = "content_item">';
      html += '<p>' + 'There is no Result' + '</p>';
      html += '</div>'
      document.getElementById('content_item').innerHTML = html;
      return;

   }
   const content = filtered_zoo.map((item, index) => {
      if (index >= start && index < end) {
         html += '<div class = "content_item">';
         html += '<figure class ="column">';
         html += '<img src =img/zoo/' + item.Pic + '>';
         html += '<figcaption>' + item.Name + '</figucaption>' + '<br>';
         html += '<a href="detail.html?id=' + item.Galary + '">' + 'Read more' + '</a>';
         html += '</figure>';
         html += '</div>'

         return html;
      }
   })
   document.getElementById('content_item').innerHTML = html;
}


