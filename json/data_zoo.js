function renderListPage(data) {
   if (!document.getElementById('number-page')) return false
   let filtered_zoo = data || data_zoo;
   totalPage = Math.ceil(filtered_zoo.length / perPage);
   let html = '';
   html += `<li class="page-item active"><a class="page-link"> ${1} </a></li>`;
   for (let i = 2; i <= totalPage; i++) {
      html += `<li class="page-item"><a class="page-link"> ${i} </a></li>`;
   }
   document.getElementById('number-page').innerHTML = html;
}
renderListPage();

function changePage() {
   const currentPage_new = document.querySelectorAll('.number-page li');
   console.log(currentPage_new);
   if (currentPage_new.length === 1) {

      $('.btn-pre').hide()
      $('.btn-next').hide()
   }
   else {
      for (let i = 0; i < currentPage_new.length; i++) {
         currentPage_new[i].addEventListener('click', () => {
            let value = i + 1;
            currentPage = value;
            $('.number-page li').removeClass('active');
            currentPage_new[i].classList.add('active');
            if (currentPage > 1 && currentPage < totalPage) {
               $('.btn-pre').removeClass('btn-active');
               $('.btn-next').removeClass('btn-active');
            }
            if (currentPage === 1) {
               $('.btn-pre').addClass('btn-active');
            }
            if (currentPage === totalPage) {
               $('.btn-next').addClass('btn-active');
            }
            getCurrentPage(currentPage);
            renderDatazoo();
         })
      }
   }
}

changePage();


